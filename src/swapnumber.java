public class swapnumber {
    public static void main(String args[]){
        int x=10 , y=20 , temp;
        System.out.println(" Before Swapping:");
        System.out.println("x =" +x);
        System.out.println("y =" +y);
        temp =x;
        x=y;
        y=temp;


        System.out.println("Aftre Swapping:");
        System.out.println("x=" +x);
        System.out.println("y=" +y);

    }
}
